// SearchHindsightNotes searches hindsight notes matching a prompt.
func (r *NoteRepository) SearchHindsightNotes(ctx context.Context, prompt, tenantID string, limit int) ([]domain.HindsightNote, error) {
	if tenantID == "" {
		tenantID = tenant.FromContext(ctx)
	}
	searchPattern := "%" + strings.ToLower(prompt) + "%"

	query := `SELECT id, tenant_id, session_id, title, error_pattern, severity,
		last_accessed_at, access_count, error_type, error_message, error_context,
		resolution, resolution_steps, resolution_reference, lessons_learned,
		prevention_strategy, occurrence_count, reference_count, prevention_success_count,
		created_by, auto_generated, created_at, updated_at, last_occurrence_at,
		prevention_verified, priority
		FROM hindsight_notes
		WHERE tenant_id = $1
		AND (LOWER(error_message) LIKE $2 OR LOWER(title) LIKE $2 OR LOWER(error_type) LIKE $2)
		ORDER BY occurrence_count DESC, created_at DESC
		LIMIT $3`

	rows, err := r.pool.Query(ctx, query, tenantID, searchPattern, limit)
	if err != nil {
		return nil, fmt.Errorf("searching hindsight notes: %w", err)
	}
	defer rows.Close()

	return r.scanHindsightRows(rows)
}

