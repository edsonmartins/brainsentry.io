// IncrementOccurrence increments the occurrence count for a hindsight note.
func (r *NoteRepository) IncrementOccurrence(ctx context.Context, id string) error {
	_, err := r.pool.Exec(ctx,
		`UPDATE hindsight_notes SET occurrence_count = occurrence_count + 1, last_occurrence_at = $2, updated_at = $2 WHERE id = $1`,
		id, time.Now())
	return err
}

func (r *NoteRepository) scanHindsightRows(rows interface{ Next() bool; Scan(...any) error }) ([]domain.HindsightNote, error) {
	var notes []domain.HindsightNote
	for rows.Next() {
		var n domain.HindsightNote
		if err := rows.Scan(
			&n.ID, &n.TenantID, &n.SessionID, &n.Title, &n.ErrorPattern, &n.Severity,
			&n.LastAccessedAt, &n.AccessCount, &n.ErrorType, &n.ErrorMessage, &n.ErrorContext,
			&n.Resolution, &n.ResolutionSteps, &n.ResolutionReference, &n.LessonsLearned,
			&n.PreventionStrategy, &n.OccurrenceCount, &n.ReferenceCount, &n.PreventionSuccessCount,
			&n.CreatedBy, &n.AutoGenerated, &n.CreatedAt, &n.UpdatedAt, &n.LastOccurrenceAt,
			&n.PreventionVerified, &n.Priority,
		); err != nil {
			return nil, fmt.Errorf("scanning hindsight note: %w", err)
		}
		notes = append(notes, n)
	}
	return notes, nil
}
