// NoteTakingService handles session analysis and note generation.
type NoteTakingService struct {
	noteRepo        *postgres.NoteRepository
	auditRepo       *postgres.AuditRepository
	observationRepo *postgres.ObservationRepository
	openRouter      *OpenRouterService
	auditService    *AuditService
}

// NewNoteTakingService creates a new NoteTakingService.
func NewNoteTakingService(
	noteRepo *postgres.NoteRepository,
	auditRepo *postgres.AuditRepository,
	openRouter *OpenRouterService,
	auditService *AuditService,
	observationRepo *postgres.ObservationRepository,
) *NoteTakingService {
	return &NoteTakingService{
		noteRepo:        noteRepo,
		auditRepo:       auditRepo,
		observationRepo: observationRepo,
		openRouter:      openRouter,
		auditService:    auditService,
	}
}

// AnalyzeSession analyzes a session and extracts insights, decisions, and failures.
func (s *NoteTakingService) AnalyzeSession(ctx context.Context, req dto.SessionAnalysisRequest) (*dto.SessionAnalysisResponse, error) {
	tenantID := tenant.FromContext(ctx)
	if req.TenantID != "" {
		tenantID = req.TenantID
	}

	// Get audit logs for the session
	auditLogs, err := s.auditService.FindBySessionID(ctx, req.SessionID, 100)
	if err != nil {
		return nil, fmt.Errorf("fetching session audit logs: %w", err)
	}

	if len(auditLogs) == 0 {
		return &dto.SessionAnalysisResponse{
			SessionID:  req.SessionID,
			TenantID:   tenantID,
			AnalyzedAt: time.Now(),
		}, nil
	}

	// Build session transcript for LLM analysis
	transcript := buildSessionTranscript(auditLogs)

	resp := &dto.SessionAnalysisResponse{
		SessionID:  req.SessionID,
		TenantID:   tenantID,
		AnalyzedAt: time.Now(),
	}

	if s.openRouter == nil {
		return resp, nil
	}

	// Extract decisions
	includeDecisions := req.IncludeDecisions == nil || *req.IncludeDecisions
	if includeDecisions {
		decisions, err := s.extractDecisions(ctx, transcript)
		if err != nil {
			slog.Warn("failed to extract decisions", "error", err)
		} else {
			resp.Decisions = decisions
			resp.TotalDecisions = len(decisions)
		}
	}

	// Extract insights
	includeInsights := req.IncludeInsights == nil || *req.IncludeInsights
	maxInsights := req.MaxInsights
	if maxInsights <= 0 {
		maxInsights = 10
	}
	if includeInsights {
		insights, err := s.extractInsights(ctx, transcript, maxInsights)
		if err != nil {
			slog.Warn("failed to extract insights", "error", err)
		} else {
			resp.Insights = insights
			resp.TotalInsights = len(insights)
		}
	}

	// Extract failures
	includeFailures := req.IncludeFailures == nil || *req.IncludeFailures
	if includeFailures {
		failures, err := s.extractFailures(ctx, transcript)
		if err != nil {
			slog.Warn("failed to extract failures", "error", err)
		} else {
			resp.Failures = failures
			resp.TotalFailures = len(failures)

			// Auto-create hindsight notes for failures
			go s.createHindsightNotesFromFailures(tenant.WithTenant(context.Background(), tenantID), req.SessionID, failures)
		}
	}

	// Auto-create notes from insights
	if len(resp.Insights) > 0 {
		go s.createNotesFromInsights(tenant.WithTenant(context.Background(), tenantID), req.SessionID, resp.Insights)
	}

	// Auto-create typed session observations
	if s.observationRepo != nil {
		go s.createObservationsFromAnalysis(tenant.WithTenant(context.Background(), tenantID), req.SessionID, resp)
	}

	return resp, nil
}

// GetObservationsBySession returns typed observations for a session.
func (s *NoteTakingService) GetObservationsBySession(ctx context.Context, sessionID string) ([]domain.SessionObservation, error) {
	if s.observationRepo == nil {
		return nil, nil
	}
	return s.observationRepo.FindBySession(ctx, sessionID)
}

// GetObservationsByType returns observations filtered by type.
func (s *NoteTakingService) GetObservationsByType(ctx context.Context, obsType domain.ObservationType, limit int) ([]domain.SessionObservation, error) {
	if s.observationRepo == nil {
		return nil, nil
	}
	return s.observationRepo.FindByType(ctx, obsType, limit)
}

func (s *NoteTakingService) createObservationsFromAnalysis(ctx context.Context, sessionID string, resp *dto.SessionAnalysisResponse) {
	for _, d := range resp.Decisions {
		obs := &domain.SessionObservation{
			SessionID:     sessionID,
			Type:          domain.ObservationDecision,
			Title:         d.Title,
			Description:   d.Description,
			Context:       d.Context,
			AutoGenerated: true,
		}
		if err := s.observationRepo.Create(ctx, obs); err != nil {
			slog.Warn("failed to create decision observation", "error", err)
		}
	}

	for _, ins := range resp.Insights {
		obsType := mapInsightCategoryToObservationType(ins.Category)
		obs := &domain.SessionObservation{
			SessionID:     sessionID,
			Type:          obsType,
			Title:         truncate(ins.Content, 120),
			Description:   ins.Content,
			Context:       ins.RelatedTo,
			AutoGenerated: true,
		}
		if err := s.observationRepo.Create(ctx, obs); err != nil {
			slog.Warn("failed to create insight observation", "error", err)
		}
	}

	for _, f := range resp.Failures {
		obs := &domain.SessionObservation{
			SessionID:     sessionID,
			Type:          domain.ObservationBugfix,
			Title:         fmt.Sprintf("[%s] %s", f.ErrorType, truncate(f.ErrorMessage, 80)),
			Description:   f.Resolution,
			Context:       f.Context,
			AutoGenerated: true,
		}
		if err := s.observationRepo.Create(ctx, obs); err != nil {
			slog.Warn("failed to create failure observation", "error", err)
		}
	}
}

func mapInsightCategoryToObservationType(category string) domain.ObservationType {
	switch category {
	case "ARCHITECTURE":
		return domain.ObservationDecision
	case "DEBUGGING":
		return domain.ObservationBugfix
	case "PERFORMANCE":
		return domain.ObservationRefactor
	default:
		return domain.ObservationDiscovery
	}
}

// CreateHindsightNote creates a manual hindsight note.
func (s *NoteTakingService) CreateHindsightNote(ctx context.Context, req dto.CreateHindsightNoteRequest) (*domain.HindsightNote, error) {
	note := &domain.HindsightNote{
		SessionID:           req.SessionID,
		Title:               fmt.Sprintf("[%s] %s", req.ErrorType, truncate(req.ErrorMessage, 80)),
		Severity:            domain.SeverityMedium,
		ErrorType:           req.ErrorType,
		ErrorMessage:        req.ErrorMessage,
		ErrorContext:        req.ErrorContext,
		Resolution:          req.Resolution,
		ResolutionSteps:     req.ResolutionSteps,
		ResolutionReference: req.ResolutionReference,
		LessonsLearned:      req.LessonsLearned,
		PreventionStrategy:  req.PreventionStrategy,
		Tags:                req.Tags,
		RelatedMemoryIDs:    req.RelatedMemoryIDs,
		Priority:            req.Priority,
		OccurrenceCount:     1,
		AutoGenerated:       false,
	}

	if req.Priority == "HIGH" || req.Priority == "CRITICAL" {
		note.Severity = domain.SeverityHigh
	}

	if err := s.noteRepo.CreateHindsightNote(ctx, note); err != nil {
		return nil, fmt.Errorf("creating hindsight note: %w", err)
	}

	return note, nil
}

