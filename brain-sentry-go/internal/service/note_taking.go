func (s *NoteTakingService) extractDecisions(ctx context.Context, transcript string) ([]dto.Decision, error) {
	prompt := fmt.Sprintf(`Analyze the following session transcript and extract key decisions made.
Respond in JSON format only:
{
  "decisions": [
    {
      "title": "brief title",
      "description": "what was decided",
      "rationale": "why this decision was made",
      "context": "surrounding context"
    }
  ]
}

Session transcript:
%s`, truncate(transcript, 4000))

	response, err := s.openRouter.Chat(ctx, []ChatMessage{
		{Role: "system", Content: "You are a session analyst. Extract decisions from development sessions. Respond with valid JSON only."},
		{Role: "user", Content: prompt},
	})
	if err != nil {
		return nil, err
	}

	var result struct {
		Decisions []dto.Decision `json:"decisions"`
	}
	if err := json.Unmarshal([]byte(cleanJSON(response)), &result); err != nil {
		return nil, fmt.Errorf("parsing decisions: %w", err)
	}
	return result.Decisions, nil
}

