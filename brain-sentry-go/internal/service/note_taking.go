func (s *NoteTakingService) createHindsightNotesFromFailures(ctx context.Context, sessionID string, failures []dto.FailureInsight) {
	for _, f := range failures {
		note := &domain.HindsightNote{
			SessionID:          sessionID,
			Title:              fmt.Sprintf("[%s] %s", f.ErrorType, truncate(f.ErrorMessage, 80)),
			Severity:           domain.SeverityMedium,
			ErrorType:          f.ErrorType,
			ErrorMessage:       f.ErrorMessage,
			ErrorContext:       f.Context,
			Resolution:         f.Resolution,
			LessonsLearned:     f.LessonsLearned,
			PreventionStrategy: f.PreventionHint,
			OccurrenceCount:    1,
			AutoGenerated:      true,
		}

		if err := s.noteRepo.CreateHindsightNote(ctx, note); err != nil {
			slog.Warn("failed to create auto hindsight note", "error", err)
		}
	}
}

func (s *NoteTakingService) createNotesFromInsights(ctx context.Context, sessionID string, insights []dto.Insight) {
	for _, ins := range insights {
		noteType := domain.NoteInsight
		if ins.Category == "ARCHITECTURE" {
			noteType = domain.NoteArchitecture
		} else if ins.Category == "BEST_PRACTICE" {
			noteType = domain.NotePattern
		}

		note := &domain.Note{
			SessionID:     sessionID,
			Type:          noteType,
			Title:         truncate(ins.Content, 120),
			Content:       ins.Content,
			Category:      domain.NoteCategoryGeneric,
			Severity:      mapImportanceToSeverity(ins.Importance),
			AutoGenerated: true,
		}
		if ins.RelatedTo != "" {
			note.Keywords = []string{ins.RelatedTo}
		}

		if err := s.noteRepo.CreateNote(ctx, note); err != nil {
			slog.Warn("failed to create auto note", "error", err)
		}
	}
}

func buildSessionTranscript(logs []domain.AuditLog) string {
	var sb []byte
	for _, log := range logs {
		entry := fmt.Sprintf("[%s] %s", log.EventType, log.Timestamp.Format(time.RFC3339))
		if log.UserRequest != "" {
			entry += fmt.Sprintf("\nRequest: %s", log.UserRequest)
		}
		if log.ErrorMessage != "" {
			entry += fmt.Sprintf("\nError: %s", log.ErrorMessage)
		}
		if log.Outcome != "" {
			entry += fmt.Sprintf("\nOutcome: %s", log.Outcome)
		}
		entry += "\n---\n"
		sb = append(sb, []byte(entry)...)
	}
	return string(sb)
}

func mapImportanceToSeverity(importance string) domain.NoteSeverity {
	switch importance {
	case "HIGH":
		return domain.SeverityHigh
	case "MEDIUM":
		return domain.SeverityMedium
	default:
		return domain.SeverityLow
	}
}
